def normalize_region(value: str | None) -> str:
    """쿼리스트링 → 유효한 region_code. 미지원 값은 전국 평균으로 폴백."""
    if value is None:
        return DEFAULT_REGION
    return value if value in REGION_CODES else DEFAULT_REGION
